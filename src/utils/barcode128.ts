function encodeCode128B(text: string): boolean[] {
  const values: number[] = [CODE128B_START];
  for (let i = 0; i < text.length; i++) {
    const code = text.charCodeAt(i) - 32;
    if (code < 0 || code > 94) continue;
    values.push(code);
  }

  let checksum = values[0];
  for (let i = 1; i < values.length; i++) {
    checksum += values[i] * i;
  }
  values.push(checksum % 103);
  values.push(CODE128_STOP);

  const bars: boolean[] = [];
  for (const val of values) {
    const pattern = PATTERNS[val];
    let isBar = true;
    for (const width of pattern) {
      for (let w = 0; w < width; w++) {
        bars.push(isBar);
      }
      isBar = !isBar;
    }
  }

  return bars;
}

export function drawBarcode(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  maxWidth: number,
  height: number
): void {
  if (!text) return;

  const bars = encodeCode128B(text);
  const totalModules = bars.length;
  const moduleWidth = Math.min(maxWidth / totalModules, 1.8);
  const barcodeWidth = moduleWidth * totalModules;

  ctx.fillStyle = '#000000';
  for (let i = 0; i < bars.length; i++) {
    if (bars[i]) {
      ctx.fillRect(
        x + i * moduleWidth,
        y,
        Math.ceil(moduleWidth),
        height
      );
    }
  }

  ctx.fillStyle = '#78716c';
  ctx.font = '500 9px system-ui, -apple-system, sans-serif';
  ctx.textAlign = 'center';
  ctx.textBaseline = 'top';
  ctx.fillText(text, x + barcodeWidth / 2, y + height + 2);
  ctx.textAlign = 'left';
}
